import pyomo.environ as pyo
import matplotlib.pyplot as plt
from matplotlib.gridspec import GridSpec

class RCPSPModel(pyo.ConcreteModel):
    def __init__(self, **data):
        super().__init__()
        self._create_params(data)
        self._create_vars()
        self._create_constraints()
        self._create_objective()
        self.tasksOrder = []

    def _create_params(self, data):
        self.planningHorizon = data["planningHorizon"]
        self.tasksCount = data["tasksCount"]
        self.resourcesCount = data["resourcesCount"]
        self.durations = data["durations"]
        self.resourceCapacities = data["resourceCapacities"]
        self.demand = data["demand"]
        self.precedenceRelations = data["precedenceRelations"]

    def _create_vars(self):
        self.executeTime = pyo.Var(within=pyo.NonNegativeIntegers)
        self.matrix = pyo.Var(pyo.RangeSet(0, self.tasksCount - 1), pyo.RangeSet(0, self.planningHorizon),
            within=pyo.Binary)
    
    def objectiveRule(self, model):
        return self.executeTime

    def firstRule(self, model, taskNumber):
        return sum(self.matrix[taskNumber, t] * t for t in range(self.planningHorizon + 1)) <= self.executeTime

    def secondRule(self, model, taskNumber):
        return sum(self.matrix[taskNumber, t] for t in range(self.planningHorizon + 1)) == 1
        
    def thirdRule(self, model, firstTask, secondTask):
        if self.precedenceRelations[firstTask][secondTask]:
            return sum(self.matrix[firstTask, t] * t for t in range(self.planningHorizon + 1)) <= \
                sum(self.matrix[secondTask, t] * t for t in range(self.planningHorizon + 1)) - self.durations[secondTask]
        else:
            return pyo.Constraint.Skip
        
    def fourRule(self, model, timeNumber, resourceNumber):
        return sum(sum(self.matrix[taskNumber, t] * self.demand[taskNumber][resourceNumber] 
            for t in range(timeNumber, min(self.planningHorizon + 1, timeNumber + self.durations[taskNumber]))) 
            for taskNumber in range(self.tasksCount)) <= self.resourceCapacities[resourceNumber]
    
    def _create_constraints(self):
        self.firstConstraint = pyo.Constraint(pyo.RangeSet(0, self.tasksCount - 1), 
            rule=self.firstRule)
        self.secondConstraint = pyo.Constraint(pyo.RangeSet(0, self.tasksCount - 1), 
            rule=self.secondRule)
        self.thidConstraint = pyo.Constraint(pyo.RangeSet(0, self.tasksCount - 1), 
            pyo.RangeSet(0, self.tasksCount - 1), rule=self.thirdRule)
        self.fourConstraint = pyo.Constraint(pyo.RangeSet(0, self.planningHorizon), 
            pyo.RangeSet(0, self.resourcesCount - 1), rule=self.fourRule)
        
    def _create_objective(self):
        self.objectiveFunction = pyo.Objective(rule=self.objectiveRule)

    def checkAnswer(self, answer):
        check = True
        for firstTask in answer:
            for secondTask in answer:
                if self.precedenceRelations[firstTask["number"]][secondTask["number"]]:
                    check = check and (firstTask["finish"] <= secondTask["start"])
                    if firstTask["finish"] > secondTask["start"]:
                        print(firstTask, secondTask)
        for time in range(self.planningHorizon + 1):
            for resourceNumber in range(self.resourcesCount):
                resouceUseCount = 0
                for task in answer:
                    if task["start"] <= time and time <= task["finish"]:
                        resouceUseCount += self.demand[task["number"]][resourceNumber]
                check = check and resouceUseCount <= self.resourceCapacities[resourceNumber]
        return check

    def solve(self, solver: str, tee: bool = False, print_details: bool = True):
        solver = pyo.SolverFactory(solver)
        solver.solve(self, tee=tee)
        print("Время выполнения всех работ: {}".format(self.executeTime.value))
        answer = []
        for i in range(1, self.tasksCount - 1):
            for j in range(self.planningHorizon + 1):
                if self.matrix[i, j].value:
                    finishTime = j
            startTime = finishTime - self.durations[i] + 1
            print("Работа {} выполняется в промежутке [{}, {}]".format(i, startTime, finishTime))
            answer.append(
                {
                    "number": i,
                    "start": startTime,
                    "finish": finishTime
                }
            )
        print("Походит ли ответ под условия задачи: {}".format(self.checkAnswer(answer)))
        if print_details:
            self.pprint()

